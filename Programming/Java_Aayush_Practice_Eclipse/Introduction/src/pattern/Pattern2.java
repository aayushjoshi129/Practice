package pattern;
import java.util.Scanner;
public class Pattern2 {

	public static void main(String[] args) {
		System.out.println("enter the no. upto which to create pattern : ");
	Scanner sc = new Scanner(System.in);
	int n = sc.nextInt();
	for (int i=1;i<=n;i++)
	{
	for (int j=1;j<=i;j++)
	{
		System.out.print("* ");
	}
	System.out.println();
	}
	}

}
